function CommandDialog({
  title = "Command Palette",
  description = "Search for a command to run...",
  children,
  className,
  open,
  onOpenChange,
}: {
  title?: string
  description?: string
  className?: string
  open?: boolean
  onOpenChange?: (open: boolean) => void
  children?: React.ReactNode
}) {
  // Minimal custom modal — radix Dialog's react-remove-scroll/aria-hidden call
  // getComputedStyle on preact's non-Element portal placeholders and throw. A
  // plain overlay + Escape/click-outside is enough for the file finder.
  React.useEffect(() => {
    if (!open) return
    const onKey = (e: KeyboardEvent) => {
      if (e.key === "Escape") onOpenChange?.(false)
    }
    window.addEventListener("keydown", onKey)
    return () => window.removeEventListener("keydown", onKey)
  }, [open, onOpenChange])

  if (!open) return null

  return (
    <div
      className="fixed inset-0 z-50 flex items-start justify-center bg-black/10 pt-[20vh] supports-backdrop-filter:backdrop-blur-xs"
      onClick={() => onOpenChange?.(false)}
    >
      {/* biome-ignore lint/a11y/useKeyWithClickEvents: Escape is handled on window above; this stops propagation only. */}
      <div
        role="dialog"
        aria-modal="true"
        aria-label={title}
        className={cn(
          "w-full max-w-[calc(100%-2rem)] overflow-hidden rounded-xl bg-popover p-0 text-popover-foreground ring-1 ring-foreground/10 outline-none sm:max-w-sm",
          className
        )}
        onClick={(e) => e.stopPropagation()}
      >
        <span className="sr-only">{description}</span>
        {children}
      </div>
    </div>
  )
}

